create database ORMS;
use ORMS;

CREATE TABLE STORE(    
	STORE_ID INTEGER NOT NULL AUTO_INCREMENT,
	STORE_NAME VARCHAR(100) NOT NULL,
	ADDRESS1 VARCHAR(50) ,
	ADDRESS2 VARCHAR(50) ,
	CITY VARCHAR(20) ,
	STATE VARCHAR(2) ,
	ZIP VARCHAR(5) ,
	PHONE VARCHAR(10),
	PRIMARY KEY(STORE_ID)
);    
    

    

CREATE TABLE CUSTOMER(    
	CUST_ID INTEGER NOT NULL AUTO_INCREMENT,
	FIRST_NAME VARCHAR(50) NOT NULL,
	LAST_NAME VARCHAR(50) NOT NULL,
	ADDRESS1 VARCHAR(50) ,
	ADDRESS2 VARCHAR(50) ,
	CITY VARCHAR(20) ,
	STATE VARCHAR(2) ,
	ZIP VARCHAR(5) ,
	PHONE VARCHAR(10) ,
	PASSWORD VARCHAR(10) NOT NULL,
	EMAIL VARCHAR(100),
	PRIMARY KEY(CUST_ID)
);    


CREATE TABLE DEPARTMENT(   
	DEPARTMENT_ID INTEGER NOT NULL AUTO_INCREMENT,
	DEPARTMENT_NAME VARCHAR(50) NOT NULL,
	PRIMARY KEY(DEPARTMENT_ID)
);   
   



CREATE TABLE ITEM(    
	ITEM_ID INTEGER NOT NULL AUTO_INCREMENT,
	ITEM_NAME VARCHAR(50) NOT NULL,
	DEPARTMENT_ID INTEGER NOT NULL,
	PRICE NUMERIC(6,2) ,
	QUANTITY NUMERIC(6,2) ,
	DESCRIPTION VARCHAR(200),
	PRIMARY KEY(ITEM_ID)
);    
    
   


CREATE TABLE TAX_RATE(    
	TAX_ID INTEGER NOT NULL AUTO_INCREMENT,
	STATE_TAX NUMERIC(7,4) NOT NULL,
	FEDERAL_TAX NUMERIC(7,4) NOT NULL,
	PRIMARY KEY(TAX_ID)
);    
        

CREATE TABLE INVOICE_MASTER(    
	INVOICE_ID INTEGER NOT NULL AUTO_INCREMENT,
	INVOICE_DATE  DATE ,
	CUST_ID INTEGER,
	SUBTOTAL NUMERIC(6,2) ,
	STATE_TAX NUMERIC(6,2),
	FEDERAL_TAX NUMERIC(6,2),
	TOTAL_AMOUNT NUMERIC(6,2),
	PRIMARY KEY(INVOICE_ID) 
);    
    
    


CREATE TABLE INVOICE_DETAIL(    
	INVOICE_ID INTEGER NOT NULL,
	ITEM_ID INTEGER NOT NULL,
	PRICE NUMERIC(6,2) ,
	TOTAL_AMOUNT NUMERIC(6,2) ,
	QUANTITY INTEGER ,	
	PRIMARY KEY(INVOICE_ID,ITEM_ID) 
);    




